                         legend.spacing = grid::unit(0.2,"cm")) +
          ggplot2::scale_y_discrete(labels = function(x) AlleleProfileR.labelwrapper(x))

        if (title) {
          p <- p + ggplot2::ggtitle(paste("Sample: ",sampleselected$Sample,"\n","Gene: ",geneselected$Gene, sep=""))
